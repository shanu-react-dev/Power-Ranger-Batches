//! string
//? String is a primitive datattype in JavaScript, it is a collection of multiple characters or sequence of characters enclosed within double quotes or single quotes or BackTicks.
var myName = "Sundari...";
console.log(myName);
console.log(typeof myName); //string

//! number
//? number is a primitive datatype in JS, represents numerical value whether it is integer or decimal value. It can store numbers upto 16 digits.
var age = 23;
console.log(age);
console.log(typeof age); //number

//! null
//? null is a primitive datatype in JS, it is a representation of an empty object. it is not zero. it is a special value or empty value. it's type is an object.
var kids = null;
console.log(kids);
console.log(typeof kids); //object

//! undefined
//? undefined is a primitive datatype in JS, it is a value which is not defined yet.
var friends = undefined;
console.log(friends);
console.log(typeof friends); //undefined

//! boolean
//? boolean is a primitive datatype in JS which contains two values true or false.
var isMarried = true;
console.log(isMarried);
console.log(typeof isMarried); //boolean

//! bigint
//? bigint is a primitive datatype in JS, which is used to store integer value greater than 16 digits.
var starsIntheSky = 12345678901234567890892n;
console.log(starsIntheSky);
console.log(typeof starsIntheSky); //bigint

//! Symbol
//? Symbol is a primitive datatype in JS, generally used for logic purpose and it is used to make a variable unique.
var myFun = Symbol;
console.log(myFun);
console.log(typeof myFun); //Function

